package com.jw.lightwallet.tx;

public class TxResult {
	
	String	tx_hash;
	String	tx_key;
	
	
	public String getTx_hash() {
		return tx_hash;
	}
	public void setTx_hash(String tx_hash) {
		this.tx_hash = tx_hash;
	}
	public String getTx_key() {
		return tx_key;
	}
	public void setTx_key(String tx_key) {
		this.tx_key = tx_key;
	}
	
	

}
